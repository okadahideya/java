public class Switch {
  public static void main(String[] args) {
    int myAge = 28;
    switch(myAge) {
      case 20:
        System.out.println("私の年齢は20歳です");
        break;
      case 25:
        System.out.println("私の年齢は25歳です");
        break;
      case 28:
        System.out.println("私の年齢は28歳です");
        break;
      default:
        System.out.println("私の年齢は秘密です");
    }
  }
}
